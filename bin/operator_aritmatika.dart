void main() {
    var a = 10;
    var b = 2;
    var c = 0;

    c = a + b;
    print('a + b = $c');

    c = a - b;
    print('a - b = $c');

    c = a * b;
    print('a * b = $c');

    c = a ~/ b;
    print('a ~/ b = $c');

    c = a % b;
    print('a % b = $c');

    c = a++;
    print('a++ = $c');

    c = a--;
    print('a-- = $c');
}